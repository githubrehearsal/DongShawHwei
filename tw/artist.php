					<p class="mb-20 ">董小蕙 (1962-
						)，台灣女畫家。台北市立第一女子中學、國立台灣師範大學美術系、美術研究所畢業。創作資歷三十餘年，國內外繪畫個展發表二十餘次。早期研究印象派畫風，多次赴歐洲遊歷創作，消化西畫色彩與光影變化之亮麗表現。念研究所時致力於中國老莊哲學之研究，寫成《莊子思想之美學意義》（臺北學生書局，1993）一書。自此在創作上深受老莊智慧之啟發，逐漸提煉與深化其繪畫內涵，也進行將人生與創作合一的自我實踐過程。</p>
					<p class="mb-20 ">1995年畫家畫下了第一張以自身居所為主題的〈庭院老樹〉，自此開啟了其後20餘年的「老院子」系列繪畫風格。就上層社會而言，西方是花園，中國則是園林，而「院子」則是一般平民安身立命的生活場域。由此，畫家只是不斷地畫下生活週遭的心境感受，卻從未標榜任何新藝術思維之表現觀念；她由西方視覺意象的光色寫生開始，而逐漸轉向為深具東方藝術性格的創作內涵。90年代後期，畫家創作風格脫離了印象派的亮麗色彩，發展出較具主觀心靈意象的「黑桌靜物」系列，以生動的花卉、簡單的器皿、木質的桌椅來組構畫面，營造出背景空間的光源變化，形成其獨特的美感意境。</p>
					<p class="mb-20 ">基於對一個時代的懷念，畫家透過畫筆將平日裡「老院子」的每一個角落，在各個不同的季節變化、晨昏晴雨，以及花木光影等院落景緻，皆留下了靜謐宜人的動人詩篇，發展出數量驚人的「老院子」各式風格系列。「老院子」畫風系列融入了中國哲思之人文美感意境，並結合西方藝術兼具「寫實性」與「抽象性」之造型理念，建立了一種融合中西的新繪畫形式。「老院子」畫風體系的成形，雖是畫家個人的一頁心靈紀實，但卻充分體現道家「靜觀」與「和諧」的美感內涵，也遠承中國文人傳統的隱逸精神。就中國繪畫的現代發展而言，畫家達到了令人訝異的高度，惟此內涵意境猶待後人做更深入的觀察與認知。</p>
					<p class="mb-20 ">2017年11月畫家董小蕙於台北國立歷史博物館「國家畫廊」舉辦《老院子•韶光•年華》大型個展，發表其跨越20多年來之10個繪畫風格系列，包括老院子之「印象-寫生系列」、「早期靜物系列」、「仿古系列」；老院子之「黑白系列」、「抽象系列」、「濃蔭系列」、「花映系列」；「黑桌靜物系列」、「水彩系列」、「素描系列」，以及多組大尺幅之油畫連作等，共計展出作品達250餘件，不但充分顯露畫家爆發性極強之創作張力，也完整地對世人展現其所建構「老院子」繪畫風格之龐大藝術體系。</p>

