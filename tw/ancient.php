		<h2>倣古工筆系列</h2><br><br>
		<p>由於體悟到「寫生」是中西繪畫相通的「共法」，更是中國畫掌握氣韻生動的不二法門，畫家曾畫過許多白描茶花的寫生手稿，於是再以工筆油畫的透明畫法，畫了一系列的紅白茶花小品。這些連作皆以寫生為主，一方面是西方的靜物構圖，具象地描寫主題，而背景空間卻是平面性的簡化效果，使畫面產生了現代感的簡約風格。另一方面花卉的姿態婉約，花葉描寫盡合生意盎然的寫實意象，全是是古人畫花卉清逸雅致的路數。畫梨花則以折枝花卉的美感意念為之，構圖精煉皆可見源自於古畫的趣味，而細觀枝葉生長的各種意態，又在在可見畫家現身說法的生動感；又以扇面形式作花卉，典雅工緻，古意盎然，饒有興味。<br><br>
		西方早於文藝復興時期的古典油畫傳統，有著十分接近中國工筆畫的表現技法，因此中世紀時的許多繪本或油畫細節，都可見到一種極其生動、細緻、古拙、典雅的美感，這種具平面性的寫實風格與中國唐宋高古的工筆畫頗有相通之趣味，或許這是畫家應由工筆油畫的方式，來嘗試畫其倣古花卉系列的原因。同理乃知，中國於明清以降的水墨寫意畫則不適合以油畫表現，於是畫家運用水彩風格畫了一系列倣古山水花卉作品；〈倣石濤花木竹石通景十二屏〉即是由此而衍生，由於畫家對石濤(1642-1707）十分心儀，此作或可視為係向石濤的致敬。<br><br>
		石濤十二通景屏原作尺幅頗大，畫家將之縮小而以水彩風格表現，用筆清逸細緻，色澤蘊藉秀潤，畫幅雖小而意境動人。暈濕的畫法有如水墨渲染，遠承中國文人寫意畫之心法，可謂另出一番境地，別具手眼。畫家曾擬倣過三套十二通景屏，可見其用功致力之深；可惜其中一套多年前遭竊，迄今未見蹤影，畫家屢屢不去於心，難以忘情，猶盼他日有緣能再度相逢。綜而觀之，畫家之倣古風格系列構圖簡潔，用筆工緻而色澤妍潤，特別有一種清逸高雅的風姿；其凝神專注的心境，尤能見與古人相通的沉靜和諧，可謂出入於古典與現代之間。<br><br>
