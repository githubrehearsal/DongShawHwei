					<p class="ptb-20 mb-5">董小蕙生於1962年，以學、術科皆第一名的優良成績畢業於師範大學美術系。<br>
						早期致力於西方印象派畫風之研究，其後專注於中國老莊哲學之探研。<br>
					董小蕙90年代後期畫風轉變，融入中國哲思之人文美感意境，創作了獨具個人風格的繪畫表現。<br>
					她的「黑桌靜物」系列，展現對舊木桌深沉樸實特質之鍾愛，是畫家靜觀及自我觀照的內心呈現，以簡約筆觸達到純粹的寧靜。<br><br>
					「老院子」系列則是畫家得知居住多年的老院子即將被拆遷，與時間賽跑，用畫筆把即將成為歷史的舊式房舍景觀紀錄下來的作品。<br><br>
					這次在雙橡園展出之畫作，均為她近年來最具代表性的「老院子」及「黑桌靜物」系列作品。<br>董小蕙表示，她的畫中，所要傳達的是其個人對生活的紀錄與感受，這是一種安身立命的訊息，是承自於中國文化內涵的情感底蘊；然而她所用的創作媒介卻是西方的油畫材質，這或許這正是台灣這塊土地所蘊育出的文化成果。<br><br>
					她很高興能有機會將這份靜謐欣悅的生命感受，在具有歷史意義的「雙橡園」中，傳遞給西方國際間的友邦人士。<br><br>
					周慧婷表示，董小蕙提供十幅畫作，雙橡園目前展出七幅。展出六個月後，或許會將畫作移至駐美代表處辦公大樓，雙橡園則另再展出其他台灣藝術家的作品。</p>
