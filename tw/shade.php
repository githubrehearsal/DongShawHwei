		<h2>老院子「濃蔭系列」</h2><br><br>
		<p>畫家的「老院子」畫風系列在2000以後，日益融入了中國哲思之人文美感意境，發展出她獨具個人特色的創作表現。2010年前後的老院子捨棄了西方式的寫生寫景，將院落中的景致做了全景或局部的景深處理，發展出「黑白系列」、「濃蔭系列」、「大樹家族」等不同的風格表現。這些新的風格應用了平面簡化的造型效果，以及對比性強的色調反差，使畫面產生更集中而有強度的結構張力。平面又立體、寫實又省略、具象又變形，既古典也現代；是西方的繪畫理念，又是中國靜觀隱逸的美感底蘊。這些相反對立的藝術創作特質，在畫家的筆下都得到協調統一的安置。<br><br>
		「濃蔭系列」係出自畫家對院落裏花木生長茂密，濃郁幽深的印象。夏日裡陽光璀璨，枝葉繁盛，光線透過林蔭空隙，交織成一片深邃動人的厚實感。因此這種美感雖然也有細緻的經營，但是偏向一種較厚重粗曠的大枝茂葉；換言之，就是比較具剛性的美感力道。2012年以後畫家這一系列的畫風漸漸有所轉向，多以林蔭間細緻的花草為主。春日裡百花齊放，生意盎然，花卉生長的形態特別清麗多姿；而不同的花卉在院落中綻放，有如競妍，亦有如相互聚語一般，於是「花映系列」油然衍生。相對而言，後者是一種婉約細麗的美感，較具柔美的女性意象。<br><br>
		花卉是自然世界中一種神奇的存在，色澤妍麗卻清雅脫俗，可謂天地間靈氣之所鍾。畫家由於特別喜愛花草植物，以之為創作題材且樂此而不疲。由此，畫家畫出了花木在四季運行生長的天趣，也畫出了靈氣與清雅。老院子中的花木植栽，既無參天古木，亦無奇花異種，都是日常所習見的植物；這使得畫面特別具有一種樸實的意趣，解消了不食人間煙火的距離。「濃蔭」與「花映」或為節氣之感應，或為主觀之意象，或為觀者之感受；綜而言之，畫家老院子創作系列是兩種美感的交織，一方面具有強度的表現張力，另一方面則有著抒情細緻的內涵，兩者間互為表裡，也是一體之兩面。<br><br>
