					<p class="mb-20 ">畫家「老院子」畫風系列，起源於畫家對自身居住環境改變的切身感受。台灣地區早年有許多日式的房舍院落，是伴隨著許多人成長的美好回憶。八○年代以後因為都市變遷發展的政商需求而大量拆除改建，這種破壞似乎是時代更替無法逆轉的宿命。因此畫家希望為該年代的人文景觀與生活感受留下一份紀錄。靜謐院落中各種花木植物隨興生長的野趣，伴隨著四季朝暮的光暈變化，呈現出對人生平常幸福懷舊珍惜的心境。</p>
					<p class="mb-20 ">「老院子」前期畫風係由印象派風格的實景寫生開始，著重於色光效果的璀璨表現。畫家前十年間的創作消化了印象派的光影色彩，用抒情細碎的筆觸畫出了陽光亮麗，色調和熙的亞熱帶情調；這種色溫光澤不同於歐洲的地理氣候，而屬於台灣地區的景觀氛圍。由此畫家畫出了她靜謐平淡的生活，真實記錄了院落中花木生長、季節轉換、光影變化的感受，也呈現了畫家對老院子的深厚情感。</p>
					<p class="mb-20 ">2005年前後，畫家的老院子繪畫風格開始有了不同的轉變，以往外光派的細緻筆觸已傾向平塗畫法；同時以往鮮明亮麗的色彩，也轉為較深沉濃郁的色調。老院子的情境氛圍依舊深刻動人，但是畫家的心境已有著更含蓄內斂的沉澱。藝術創作風格的發展，通常是隨著畫家的生命感受一層一層地轉進，畫家不同階段的「老院子」畫風表現，最能清楚看到這種轉變的軌跡。</p>
					
					
