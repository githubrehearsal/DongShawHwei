		<h2>老院子之「黑白系列」</h2><br><br>
		<p>畫家「黑白系列」繪畫風格之產生，是來自於三個美感因素的激盪與啟發；一是畫家長年致力於素描的鍛鍊，二是早年接觸木刻版畫的學習經驗，三是超脫色彩之素樸美感的興趣。<br><br>
			由於畫家一直喜歡鋼筆線條隨興簡潔的筆調趣味，同時也發現假如沒有視覺意象的色彩紛擾，老院子本身可以提煉出一種更純粹的造型美感，生動、樸拙又有力量；她又想到早年製作版畫時，高反差所產生的特徵；於是嘗試將這種新的理念特色，運用不同的繪畫技法發揮在油畫創作上。<br><br>
			2008年畫家開始以單純的黑白油彩來表現老院子，畫面先以黑色油彩打底，再塗上白色顏料，在白色油彩未乾之際，以畫刀在畫布上刮劃出屋宇、門窗、老樹、花木，並且在遠近處的屋瓦牆面上製作各種斑駁古拙的痕跡觸感，分黑佈白，錯落有緻。<br><br>
			畫家的表現技法十分具有拙趣，而背景空間的處理也運用暈染的明暗反差效果，來營造出空間光影氛圍的真實感。畫面上運用黑白對比的反差，增添了視覺強度的醒目效果，而花草盆栽等細節的生動描繪，又不失隨興揮灑的自然天趣。<br><br>
			就風格表現來看，畫家主要是運用了西方版畫的造型特色，以及現代繪畫著重紋路肌理的筆觸效果；而另一方面花木植物的生長姿態與用筆方式，皆可見到係來自於中國書畫的筆法趣味。因此畫家可謂交互運用了中西兩種不同的藝術特質，呈現出黑白互置，層次豐富的意趣與質感。這種著重線描的黑白繪畫風格，特別能傳達一種時間消逝的歲月痕跡，簡約、寂靜、素樸、單純，在平凡無奇中卻又韻味醇厚，流露著懷舊而深刻的人文情感。<br><br>
		</p>
