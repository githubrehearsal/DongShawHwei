					<p class="mb-20 ">The artist’s Courtyard series has its origins in the changes affecting her in her personal living environment. In early years, Taiwan had a large number of Japanese-style houses with courtyards, which contributed to fond memories for many people growing up then. After the 80s, due to the commercial needs of the rapid development of Taiwan’s urban centers, such houses were demolished and restructured en masse; this kind of destruction is a seemingly inescapable fate of the changing times. Thus, Tung wished to record a small piece of the cultural landscape and feeling of life of that time. The rustic charm of the various flowers and other plants casually growing in the calm courtyard, along with the light changing with the days and seasons, embody the kind of mood one cherishingly reminisces about.</p>
					<p class="mb-20 ">In the beginning, the Courtyard series started with a style of impressionist sketching of surrounding scenery, but soon turned to a dazzling style emphasizing the effects of light and color. Tung studied and absorbed impressionism’s use of light and color over the last 10 years, and used expressive and fragmented brushstrokes to paint bright and beautiful sunlight, the tones and lights of the mood fitting the subtropics. Such color temperatures and light qualities are different from European geographic and weather features, and are part of the Taiwanese landscape and ambience. And thus Tung painted her own calm and ordinary life, and has authentically captured the plants and trees growing in the courtyard, the change of the seasons, and the feel of the interplay of light and shadow. Through this, she has also presented her own feelings toward the Courtyard.</p>
					<p class="mb-20 ">Around 2005, the artist’s Courtyard series began to have a different change, from the fragmented brushstrokes of en plain air to a style of filling in large areas with broad strokes. At the same time, formerly bright colors turned into more deep and rich tones. The atmosphere of the Courtyard is still deeply moving, but the artist’s mood has already settled into a more implicit and reserved state. The development of an artist’s creative mood and style usually follows his or her life’s experiences gaining layer after layer. Shaw-Hwei Dong’s different phases of the Courtyard series are a clear example of where such a change is visible.</p>
					
					
