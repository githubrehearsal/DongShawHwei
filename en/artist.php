					<p class="mb-20 ">Dong Shaw-hwei (1962-)，Dong Shaw-hwei is a female Taiwanese painter. Graduated from Taipei First Girls’ High School in 1980, and from the Fine Art Department of National Taiwan Normal University (NTNU), Taipei, in 1984, with a Master of Fine Arts in 1992 from NTNU. More than 30 years of creative experience, more than 20 domestic and international solo exhibitions. Start out studying impressionism, and frequently went to Europe for her craft, absorbing the way Western paintings displayed the interplay of color and light and shadow. Devoted herself to the study of Zhuangzi’s philosophy while studying art in graduate school, and eventually published “Aesthetic Significance of Zhuangzi’s Thoughts” (Taipei Student Book Co. Ltd, 1993). Since then has received no small amount of inspiration from Laozi and Zhuangzi for her paintings, gradually refining and adding depth to her painting style, and also started a process of self-realization through uniting her life and her art.</p>
					<p class="mb-20 ">In the latter part of the 90’s her art shifted, incorporating the cultural aesthetics of Chinese philosophical thought, and developing her uniquely personal style with the “Still Life of Black Table Series”, as well as the “Courtyard in Black and White Series”, “Dense Leafy Shade Series” and “Great Tree Family Series” from the the “Courtyard” line. Starting with a Western concrete still life style based on visual perception, she gradually moved onto style based on the internal emotions from Chinese culture. In the process of pursuing an artistic mood of harmony and tranquility between East and West, she approached the spirit of the hermits of traditional Chinese literati, allowing her to establish a complete personal art style, and allowed her to remain outside the influence of the art circles of that time leaning toward intensifying and clashing art crazes.</p>
					<p class="mb-20 ">In recent years, the results of her creative work has garnered the attention of various art groups, and was invited multiple times to both domestic and international exhibitions, including a 2011 large-scale solo exhibition at the Sichuan Museum in Chengdu, China and a 2013 solo exhibition at the historic site of Twin Oaks in Washington DC, USA. Her art has also appeared numerous times in Hong Kong and the Art Taipei exhibition in Taiwan. This time, her creations from the past 20-plus years are displayed at the second floor national gallery of the National Museum of History Taipei. Aside from the aforementioned themes of the Courtyard and Still Life, there are paintings with various themes of “sketches”, “abstract art”, “watercolor”, “line drawing”, “old style”, and “flower projection” being presented for the first time, with a grand total of over 250 paintings.</p>
					
