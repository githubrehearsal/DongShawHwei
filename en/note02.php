					<p class="mb-20 ">In recent years, the artist has derived the “White Table” style of series out of the “Black Table” series, turning the paintings more bright and lighthearted, giving off a fresh, cheerful, and elegant feeling. Simultaneously, she also created continuous canvas paintings, with no lack of large scale pieces among them. Due to intrinsic restrictions, still life paintings are rarely painted on canvasses larger than two meters a side. However, the artist uses the composition of the elements in the painting to deal with the image, thus ever approaching a modern thought of artistic production, in order to properly complete this new style of still life paintings. Concerning female artists, paintings filled with intense power are few and far between, but the confidence and boldness Dong demonstrates here is a display rarely seen elsewhere.<br><br>
                                      In conclusion, on the one hand the artist’s still life paintings draw from themes of Western art tradition, displaying the long-lasting and profound influence of Western painting. On the other hand, they also encompass the many aesthetic details contained in CHinese culture. THrough the interpretation and depiction of simple imagery, the paintings display the realizations of the Taoist masters Laozi and Zhuangzi, contemplating the outside world and the self. A kind of carefree interest in life emerges, and also brings about a calm and comforting type of mental therapy in the observer. The paintings contain a delicate aesthetic expressed through feminine grace, while at the same time possessing a powerful tension in the shapes, with a simple rustic kind of flow between them. This so-called “merging of East and West, coupling of strength and gentleness” as a creative concept has essentially received a fitting interpretation through the artist’s creative work.</p>


